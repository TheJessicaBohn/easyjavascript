function somar (num1, num2){
    return num1 + num2
}
function subtrair (num1, num2){
    return num1 - num2
}
function multiplicar (num1, num2){
    return num1 * num2
}
function dividir (num1, num2){
    return num1 / num2
}
console.log(somar(10, 50))
console.log(subtrair(10, 50))
console.log(multiplicar(10, 50))
console.log(dividir(10, 50))
